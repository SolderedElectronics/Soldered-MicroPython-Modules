# BMP280 I2C addresses
BMP280_I2C_ADDR = 0x77
BMP280_I2C_ALT_ADDR = 0x76
BMP280_ID = 0x58

# Registers
BMP280_CHIP_ID = 0xD0
BMP280_RESET = 0xE0
BMP280_STATUS = 0xF3
BMP280_CTRL_MEAS = 0xF4
BMP280_CONFIG = 0xF5
BMP280_PRESS_MSB = 0xF7
BMP280_TEMP_MSB = 0xFA
BMP280_CALIB = 0x88

# Reset command
RESET_CODE = 0xB6

# Modes
SLEEP_MODE = 0x00
FORCED_MODE = 0x01
NORMAL_MODE = 0x03

# Oversampling
OVERSAMPLING_SKIP = 0x00
OVERSAMPLING_X1 = 0x01
OVERSAMPLING_X2 = 0x02
OVERSAMPLING_X4 = 0x03
OVERSAMPLING_X8 = 0x04
OVERSAMPLING_X16 = 0x05

# Standby time
STANDBY_0_5MS = 0x00
STANDBY_62_5MS = 0x01
STANDBY_125MS = 0x02
STANDBY_250MS = 0x03
STANDBY_500MS = 0x04
STANDBY_1000MS = 0x05
STANDBY_2000MS = 0x06
STANDBY_4000MS = 0x07

# IIR filter
IIR_FILTER_OFF = 0x00
IIR_FILTER_2 = 0x01
IIR_FILTER_4 = 0x02
IIR_FILTER_8 = 0x03
IIR_FILTER_16 = 0x04
